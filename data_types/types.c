    float g = 10.0f;

/* 
Результаты вывода:
---
Pазмер char 1
Pазмер int 4
Pазмер float 4
Pазмер double 8
Pазмер short int 2
Pазмер long int 8
Pазмер long long int 8
Pазмер double 8
Pазмер long double 16
---
*/

// Преобразование типов
    // явные преобразования (в скобках указываем тип, в который нужно преобразовать)
    int ab = (int)10.0f; // преобразование в целочисленное из вещественного
    float abc = (float)10ul; // преобразование в вещественное из целочисленного
    double abcd = (double)10ll; // преобразование в вещественное из целочисленного

