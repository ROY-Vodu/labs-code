    // приметяться суффиксы:
    // u (U) - беззнаковые
    // l (L) - 8 байт
    // ul (UL) - беззнаковые 8 байт
    // ll (LL) - 8 байт
    // ull (ULL) - беззнаковые 8 байт
    // f - вещественные
    long int d = 10L;
    unsigned long int dd = 10UL;
    signed long int ddd = 10l;
    
    long long int e = 10ll;
    unsigned long long int ee = 10ULL;
